package main

import (
	"database/sql"
	"fmt"
	"log"
	"net/http"
	"os"
	"time"

	_ "github.com/lib/pq"
	httpSwagger "github.com/swaggo/http-swagger"

	_ "multitrackticketing/docs" // This will be generated by swag init
	delivery "multitrackticketing/internal/delivery/http"
	"multitrackticketing/internal/repository/postgres"
	"multitrackticketing/internal/usecase"
)

// @title Multi-Track Ticketing API
// @version 1.0
// @description This is the backend API for a conference ticketing system.
// @host localhost:8080
// @BasePath /
func main() {
	// 1. Database Connection
	connStr := "postgres://postgres:postgres@localhost:5432/multitrackticketing?sslmode=disable"
	if url := os.Getenv("DATABASE_URL"); url != "" {
		connStr = url
	}

	db, err := sql.Open("postgres", connStr)
	if err != nil {
		log.Fatal(err)
	}
	defer db.Close()

	if err := db.Ping(); err != nil {
		log.Fatal("Could not connect to database:", err)
	}
	fmt.Println("Connected to Database")

	// 2. Init Layers
	sessionRepo := postgres.NewSessionRepository(db)
	manageUseCase := usecase.NewManageScheduleUseCase(sessionRepo, 2*time.Second)
	scheduleController := delivery.NewScheduleController(manageUseCase)

	// 3. Router
	mux := http.NewServeMux()

	// API Routes
	mux.HandleFunc("POST /sessions", scheduleController.CreateSession)

	// Swagger
	mux.Handle("/swagger/", httpSwagger.WrapHandler)

	// 4. Server
	port := ":8080"
	fmt.Printf("Server starting on %s\n", port)
	if err := http.ListenAndServe(port, mux); err != nil {
		log.Fatal(err)
	}
}
